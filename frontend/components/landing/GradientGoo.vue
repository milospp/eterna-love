function changeGradinetBg(data: any, colorName: string) {
    let newVal = data.target.value;
    document.documentElement.style.setProperty(colorName, newVal);
}

function changeGradinetColor(data: any, colorName: string) {
    console.log(data.target.value);

    let color = data.target.value;
    const hex = color.slice(1);
    const bigint = parseInt(hex, 16);
    const r = (bigint >> 16) & 255;
    const g = (bigint >> 8) & 255;
    const b = bigint & 255;
    let newVal = `${r}, ${g}, ${b}`;

    document.documentElement.style.setProperty(colorName, newVal);
}


onMounted(() => {
    const interBubble = document.querySelector<HTMLDivElement>(".interactive")!;
    let curX = 0;
    let curY = 0;
    let tgX = 0;
    let tgY = 0;

    function move() {
        curX += (tgX - curX) / 20;
        curY += (tgY - curY) / 20;
        interBubble.style.transform = `translate(${Math.round(
            curX
        )}px, ${Math.round(curY)}px)`;
        requestAnimationFrame(() => {
            move();
        });
    }

    window.addEventListener("mousemove", (event) => {
        tgX = event.clientX;
        tgY = event.clientY;
    });

    move();
});
